package courses.model;

public class Course {
	private String courseName;
	private String quiz;
	private String homework;
	private String finalDate;
	private String readingQuiz;
	
	private String quizTime;
	private String hwDue;
	private String readingDue;
	
	private String quizInfo;
	private String hwInfo;
	private String readingInfo;
	
	public String getQuizTime() {
		return quizTime;
	}
	public void setQuizTime(String quizTime) {
		this.quizTime = quizTime;
	}
	public String getHwDue() {
		return hwDue;
	}
	public void setHwDue(String hwDue) {
		this.hwDue = hwDue;
	}
	public String getReadingDue() {
		return readingDue;
	}
	public void setReadingDue(String readingDue) {
		this.readingDue = readingDue;
	}
	public String getQuizInfo() {
		return quizInfo;
	}
	public void setQuizInfo(String quizInfo) {
		this.quizInfo = quizInfo;
	}
	public String getHwInfo() {
		return hwInfo;
	}
	public void setHwInfo(String hwInfo) {
		this.hwInfo = hwInfo;
	}
	public String getReadingInfo() {
		return readingInfo;
	}
	public void setReadingInfo(String readingInfo) {
		this.readingInfo = readingInfo;
	}
	public String getReadingQuiz() {
		return readingQuiz;
	}
	public void setReadingQuiz(String readingQuiz) {
		this.readingQuiz = readingQuiz;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getQuiz() {
		return quiz;
	}
	public void setQuiz(String quiz) {
		this.quiz = quiz;
	}
	public String getHomework() {
		return homework;
	}
	public void setHomework(String homework) {
		this.homework = homework;
	}
	public String getFinalDate() {
		return finalDate;
	}
	public void setFinalDate(String finalDate) {
		this.finalDate = finalDate;
	}
	
	
}
